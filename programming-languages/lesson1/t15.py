# 5. Напишите программу для построения гистограмм. Формат
# входных данных: количество статистических измерений,
# затем сами результаты. Формат выходных данных: интервал,
# количество результатов на интервале.


def remember(number, mind={}):
    mind.setdefault(number, 0)
    mind[number] += 1


def main():
    input()
    list(map(remember, input().split()))
    return '\n'.join(
        map(
            lambda item: f'{item[0]}: {item[1]}',
            remember.__defaults__[0].items()
        )
    )


if __name__ == '__main__':
    print(main())

